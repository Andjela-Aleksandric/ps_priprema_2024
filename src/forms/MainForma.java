    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        // TODO add your handling code here:
        String errorMessage = "";
        if(jTextFieldIme.getText() == null || jTextFieldIme.getText().isEmpty()){
            errorMessage += "Niste uneli ime profesora \n";
        }
        if(jTextFieldPrezime.getText() == null || jTextFieldIme.getText().isEmpty()){
            errorMessage += "Niste uneli prezime profesora \n";
        }
        if(jComboBoxZvanje.getSelectedItem() == null){
            errorMessage += "Niste izabrali zvanje profesora \n";
        }
        if(jTextFieldEmail.getText() == null){
            errorMessage += "Niste uneli email profesora \n";
        }
        
        if(!errorMessage.isEmpty()){
            JOptionPane.showMessageDialog(this, errorMessage, "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }else{
            Profesor p = new Profesor(null, jTextFieldIme.getText(), jTextFieldPrezime.getText(), jTextFieldEmail.getText(), (Zvanje)jComboBoxZvanje.getSelectedItem());
            if(listaProfesora.contains(p)){
                JOptionPane.showMessageDialog(this, "Profesor vec postoji u bazi", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try {
                controller.dodajProfesora(p);
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Neuspesno unet profesor u bazu " + ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, "Uspesno unet profesor u bazu", "Uspesno", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

